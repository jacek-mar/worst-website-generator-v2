export type ColorScheme = {
  id: string;
  name: string;
  background: string;
  backgroundSecondary: string;
  surface: string;
  text: string;
  textSecondary: string;
  textMuted: string;
  accent1: string;
  accent2: string;
  accent3: string;
  border: string;
  glow: string;
  danger: string;
  success: string;
};

export function schemeToCssVars(s: ColorScheme): string {
  return [
    `--bg: ${s.background}`,
    `--bg2: ${s.backgroundSecondary}`,
    `--surface: ${s.surface}`,
    `--text: ${s.text}`,
    `--text2: ${s.textSecondary}`,
    `--muted: ${s.textMuted}`,
    `--a1: ${s.accent1}`,
    `--a2: ${s.accent2}`,
    `--a3: ${s.accent3}`,
    `--border: ${s.border}`,
    `--glow: ${s.glow}`,
    `--danger: ${s.danger}`,
    `--success: ${s.success}`,
  ].join('; ');
}

export function getSchemeById(id: string): ColorScheme {
  return COLOR_SCHEMES.find((s) => s.id === id) ?? COLOR_SCHEMES[0];
}

